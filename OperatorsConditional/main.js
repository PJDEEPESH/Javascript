let a = 1;
let b = 10;
console.log("a + b = ",a + b);
console.log("a - b = ",a - b);
console.log("a * b = ",a * b);
console.log("a / b = ",a / b);
console.log("a % b = ",a % b);
console.log("a ** b = ",a ** b);
console.log("--a = ",++a);//pre increment(it updates before printing or ending the statement)
console.log("--b = ",--b);//pre decrement
console.log("a++ = ",a++);//post increment(it prints or complete the stement first after that it will increment or decrement )
console.log("b-- = ",b--);//post decrement
console.log("After : ",a);
console.log("After : ",b);